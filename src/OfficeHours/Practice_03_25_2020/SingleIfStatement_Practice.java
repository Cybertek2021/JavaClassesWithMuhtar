package OfficeHours.Practice_03_25_2020;

public class SingleIfStatement_Practice {

    public static void main(String[] args) {

        // we use single if statement when we have only one condition

        if (true){
            System.out.println("True");
        }

        if (!true){
            System.out.println("True");
        }

        if (8>9){
            System.out.println("8 is greater than 9"); // not true so the statement will not get executed
        }

        if (8<9){
            System.out.println("8 is less than 9"); // true so the statement will get executed
        }

        if (8==9){
            System.out.println("8 is equal to 9"); // not true so it will not get executed or printed
        }





    }
    }
