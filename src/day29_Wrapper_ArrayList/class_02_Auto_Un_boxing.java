package day29_Wrapper_ArrayList;

public class class_02_Auto_Un_boxing {
    public static void main(String[] args) {

        /*
        Autoboxing: Whenever you assign primitive value to wrapper class. Converting primitive values to wrapper class.
            int a = 100; Integer b = a;  // Autoboxing
        Unboxing: whenever you assign wrapper class to primitive. Converting wrapper class to primitive.
            Byte Byte1 = 98; short short1 = Byte1; int int1 = Byte1

        */

        int a = 100;
        Integer b = a;  // Autoboxing

        Byte Byte1 = 98;
        short short1 = Byte1;  // Unboxing
        int int1 = Byte1;  // Unboxing
        byte byte1 = Byte1;  // Unboxing



    }


}
