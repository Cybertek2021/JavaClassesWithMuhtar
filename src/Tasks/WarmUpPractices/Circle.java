package Tasks.WarmUpPractices;

public class Circle {

    public static void main(String[] args) {

        //Area A=πr2
        //Circumference C=2πr or perimeter

        double radius = 5.5;
        double areaOfCircle= 3.14*radius*radius;
        double circumferenceOfCircle = 3.14*2*radius;


        System.out.print("If the radius of a circle equals: " + radius+ ", then Circumference is: " +circumferenceOfCircle);
        System.out.print(", and Area is: "+areaOfCircle);



    }

}
